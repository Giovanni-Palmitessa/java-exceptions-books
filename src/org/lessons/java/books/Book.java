package org.lessons.java.books;

public class Book {
    //ATTRIBUTI
    private String title;
    private int pages;
    private String author;
    private String publisher;

    // COSTRUTTORE
    public Book(String title, int pages, String author, String publisher) {
        this.title = title;
        this.pages = pages;
        this.author = author;
        this.publisher = publisher;
    }
}
